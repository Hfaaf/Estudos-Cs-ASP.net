class Program
{
    // O ponto de entrada principal para a aplicação.
    static void Main(string[] args)
    {

        // Este programa imprime "Hello, World!" na tela.
        // Console.WriteLine é um método da classe Console que escreve uma linha de texto no console.
        Console.WriteLine("Hello, World!");

        // O método Console.ReadKey() aguarda o usuário pressionar uma tecla antes de fechar o console.
        Console.ReadKey();
    }
}
